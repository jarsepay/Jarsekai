	var replacer = []
	xStr.map((v, i) => replacer.push({
		original: v,
		convert: yStr[style][i]
	}))
	var str = text.toLowerCase().split('')
	var output = []
	str.map(v => {
		const find = replacer.find(x => x.original == v)
		find ? output.push(find.convert) : output.push(v)
	})
	return output.join('')
